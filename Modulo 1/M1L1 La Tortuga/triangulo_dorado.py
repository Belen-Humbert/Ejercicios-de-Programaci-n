# Utiliza la tortuga para dibujar un triángulo dorado.
# Para ese triángulo, la tortuga debe girar 120 grados tres veces.
# ¿Puedes recordar qué comando establece el color de la tortuga?
# Recuerda que el comando t.color("color") establece el color que la tortuga usará para dibujar.

import turtle

t = turtle.Turtle()
t.shape("turtle")
# Ingrese su código a continuación
t.color("gold")
t.forward(150)
t.left(120)
t.forward(150)
t.left(120)
t.forward(150)
t.left(120)