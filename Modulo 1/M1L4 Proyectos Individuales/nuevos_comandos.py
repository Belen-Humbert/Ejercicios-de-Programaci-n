t.rt(90)
t.fd(40)
t.lt(90)
t.fd(40)
t.rt(90)
t.fd(40)
t.lt(90)
t.fd(40)
t.rt(90)
t.fd(40)
t.lt(90)
t.fd(40)
t.rt(90)
t.fd(40)
t.rt(90)
t.fd(160)
t.rt(90)
t.fd(160)
t.rt(45)
